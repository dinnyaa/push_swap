#include "libft.h"

int		ft_atoi(const char *str)
{
	int sign;
	long result;

	sign = 1;
	result = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		result = result * 10 + ((*str) - '0');
		str++;
	}
	if (result < INT_MIN || result > INT_MAX)
		ft_error("the number is out of range");
	return (sign * result);
}
